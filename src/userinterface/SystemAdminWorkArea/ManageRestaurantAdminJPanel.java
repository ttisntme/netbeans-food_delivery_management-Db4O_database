    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        
        Restaurant restaurant = (Restaurant) networkJComboBox.getSelectedItem();
        
        String username = usernameJTextField.getText();
        String password = String.valueOf(passwordJPasswordField.getPassword());
        String name = nameJTextField.getText();
        
        Employee employee = system.getEmployeeDirectory().createEmployee(name);
        
        
        UserAccount ua = system.getUserAccountDirectory().createUserAccount(username, password, employee, new AdminRole());
        
        ua.setRestaurantID(restaurant.getId());
//        JOptionPane.showMessageDialog(this,"input admin's restaurantID" + ua.getRole().getRestaurantID());
        
        populateTable();
        
    }//GEN-LAST:event_submitJButtonActionPerformed

