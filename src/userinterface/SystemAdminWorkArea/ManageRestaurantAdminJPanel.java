    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = enterpriseJTable.getSelectedRow();

        if (selectedRow < 0){
            return;
        }

        UserAccount ua = (UserAccount)enterpriseJTable.getValueAt(selectedRow, 2);
        system.getUserAccountDirectory().getUserAccountList().remove(ua);
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = enterpriseJTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Pleause choose one restaurant!");
            return;
        }
        String newName = txtNewName.getText();
        UserAccount ua = (UserAccount)enterpriseJTable.getValueAt(selectedRow, 2);
        ua.getEmployee().setName(newName);
        txtNewName.setText("");
        populateTable();
    }//GEN-LAST:event_btnUpdateActionPerformed

