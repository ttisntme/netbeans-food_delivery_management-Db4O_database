    public void populateEmployeeComboBox(Role role){
        employeeJComboBox.removeAllItems();
        if(role.toString().equals("Business.Role.DeliverManRole")) {
            for(DeliveryMan dm : system.getDeliveryManDirectory().getDeliveryManList()) {
                employeeJComboBox.addItem(dm);
            }
        }else if(role.toString().equals("Business.Role.CustomerRole")) {
            for(Customer cu : system.getCustomerDirectory().getCustomerList()) {
                employeeJComboBox.addItem(cu);
            }
        }
        
    }
    
    private void populateRoleComboBox(EcoSystem system){
        roleJComboBox.removeAllItems();
        
        roleJComboBox.addItem(new DeliverManRole());
        roleJComboBox.addItem(new CustomerRole());
    }

    public void popData() {

        DefaultTableModel model = (DefaultTableModel) userJTable.getModel();

        model.setRowCount(0);

        for (UserAccount ua : system.getUserAccountDirectory().getUserAccountList()) {
//          JOptionPane.showMessageDialog(this,userAccount.getRole().toString());
            if(ua.getRole().toString().equals("Business.Role.DeliverManRole") || ua.getRole().toString().equals("Business.Role.CustomerRole")) {
//              JOptionPane.showMessageDialog(this,userAccount.getRole().toString());
                Object row[] = new Object[3];
                row[0] = ua;
                row[1] = ua.getRole();
                if(ua.getRole().toString().equals("Business.Role.DeliverManRole")) row[2] = ua.getDeliveryManID();
                else row[2] = ua.getCustomerID();
                

                model.addRow(row);
            }
        }
    }

