    private void createUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserJButtonActionPerformed
        String userName = nameJTextField.getText();
        String password = passwordJTextField.getText();
        
        String name = employeeJComboBox.getSelectedItem().toString();
        
        Employee employee = system.getEmployeeDirectory().createEmployee(name);
        Role role = (Role) roleJComboBox.getSelectedItem();
        
        UserAccount ua = system.getUserAccountDirectory().createUserAccount(userName, password, employee, role);
        if(role.toString().equals("Business.Role.DeliverManRole")) {
            DeliveryMan dm = system.getDeliveryManDirectory().getDeliveryManByName(name);
            ua.setDeliveryManID(dm.getId());
//            JOptionPane.showMessageDialog(this, "delivery id = " + ua.getRole().getDeliveryManID());
        }else if(role.toString().equals("Business.Role.CustomerRole")) {
            Customer c = system.getCustomerDirectory().getCustomerByName(name);
            ua.setCustomerID(c.getId());
//            JOptionPane.showMessageDialog(this, "customer id = " + ua.getRole().getCustomerID());

        }
        
        popData();
    }//GEN-LAST:event_createUserJButtonActionPerformed

